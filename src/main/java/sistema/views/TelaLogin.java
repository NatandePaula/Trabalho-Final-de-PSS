    // M√©todo para fazer login
    private void fazerLogin() {
        String email = txtEmail.getText().trim();
        String senha = new String(txtSenha.getPassword());

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite seu e-mail.", "Campo Obrigat√≥rio", JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
            return;
        }

        if (senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite sua senha.", "Campo Obrigat√≥rio", JOptionPane.WARNING_MESSAGE);
            txtSenha.requestFocus();
            return;
        }

        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um e-mail v√°lido.", "E-mail Inv√°lido", JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
            return;
        }

        try {
            if (email.equals("admin@teste.com") && senha.equals("123456")) {
                JOptionPane.showMessageDialog(this, "Login realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                abrirTelaCatalogo();
            } else if (email.equals("usuario@teste.com") && senha.equals("123456")) {
                JOptionPane.showMessageDialog(this, "Login realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                abrirTelaCatalogo();
            } else {
                JOptionPane.showMessageDialog(this, "E-mail ou senha incorretos.", "Erro de Login", JOptionPane.ERROR_MESSAGE);
                txtSenha.setText("");
                txtSenha.requestFocus();
            }
        } catch (Exception ex) {
            logger.severe("Erro durante o login: " + ex.getMessage());
            JOptionPane.showMessageDialog(this, "Erro interno do sistema. Tente novamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

